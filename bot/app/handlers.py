        response_text = f"‚úÖ –°–æ—Ö—Ä–∞–Ω–µ–Ω–æ —Ñ–∞–π–ª–æ–≤: {files_count}"
        logger.info(f"–°–æ—Ö—Ä–∞–Ω–µ–Ω–æ {files_count} —Ñ–∞–π–ª–æ–≤ –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è {user_id}")
